/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	static uint16_t old_bright = 63;
	static bool smooth = true;

  /* USER CODE END 1 */
  

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM3_Init();
  MX_TIM1_Init();
  /* USER CODE BEGIN 2 */
  HAL_Delay(1000);
  HAL_GPIO_WritePin(PWR_GPIO_Port, PWR_Pin, GPIO_PIN_SET);

  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_3);
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_2);

  RENC_createButton(&renc, ENC_BUTTON_GPIO_Port, ENC_BUTTON_Pin);
  RENC_createEncoder(&renc, ENC_MAIN_GPIO_Port, ENC_MAIN_Pin, ENC_SECOND_GPIO_Port, ENC_SECOND_Pin);
  RENC_resetEncoder(&renc, old_bright, 1, 191, 1, 5, false);
  showLed(old_bright);
  init_complete = true;
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  uint16_t bright = RENC_read(&renc);
	  if (bright != old_bright) {
		  old_bright = bright;
		  if (smooth) {
			  showLed(bright);
		  } else {
			  showFullLed(bright);
		  }
	  }

	  uint8_t b_status = RENC_buttonStatus(&renc);
	  switch (b_status) {
	  	  case 1:										// Short press
	  		  smooth = !smooth;
	  		  if (smooth) {
	  			  old_bright = bright * 64 + 32;
	  			  RENC_resetEncoder(&renc, old_bright, 0, 191, 1, 5, false);
	  			  showLed(old_bright);
	  		  } else {
	  			  old_bright = bright / 64;
	  			  RENC_resetEncoder(&renc, old_bright, 0, 2, 1, 1, false);
	  			  showFullLed(old_bright);
	  		  }
	  		  break;
	  	  case 2:										// Long press, switch off the power
	  		  showLed(0);
	  		  HAL_Delay(5000);							// Wait to release the encoder
	  		  HAL_GPIO_WritePin(PWR_GPIO_Port, PWR_Pin, GPIO_PIN_RESET);
	  		  break;
	  	  default:										// Not pressed
	  		  break;
	  }

	  HAL_Delay(100);

  }
  /* USER CODE END 3 */
}

