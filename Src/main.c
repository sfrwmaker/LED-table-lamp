// Encoder Rotated
void EXTI2_3_IRQHandler(void) {
	if (!init_complete) {
		HAL_GPIO_EXTI_IRQHandler(ENC_MAIN_Pin);
		return;
	}
	RENC_encoderIntr(&renc);
	__HAL_GPIO_EXTI_CLEAR_IT(ENC_MAIN_Pin);
}

void showLed(uint16_t bright) {
	if (bright < 64) {
		TIM3->CCR2 = bright & 0x3F;						// The first led only
		TIM1->CCR2 = 0;
		TIM1->CCR3 = 0;
	} else if (bright < 128) {
		bright -= 64;
		TIM3->CCR2 = 0x3F;								// Full power on the first led
		TIM1->CCR2 = bright & 0x3F;						// Turn on the second led
		TIM1->CCR3 = 0;
	} else {
		bright -= 128;
		TIM3->CCR2 = 0x3F;								// Full power on the first led
		TIM1->CCR2 = 0x3F;								// Full power on the second led
		TIM1->CCR3 = bright & 0x3F;						// Turn on the third led
	}

}

void showFullLed(uint16_t bright) {						// bright = [0..2]
	switch (bright) {
		case 0:
			TIM3->CCR2 = 0x3F;							// The first led only
			TIM1->CCR2 = 0;
			TIM1->CCR3 = 0;
			break;
		case 1:
			TIM3->CCR2 = 0x3F;							// Full power on the first led
			TIM1->CCR2 = 0x3F;							// Turn on the second led
			TIM1->CCR3 = 0;
			break;
		case 2:
			TIM3->CCR2 = 0x3F;							// Full power on the first led
			TIM1->CCR2 = 0x3F;							// Full power on the second led
			TIM1->CCR3 = 0x3F;							// Turn on the third led
			break;
		default:
			TIM3->CCR2 = 0;								// The the light off
			TIM1->CCR2 = 0;
			TIM1->CCR3 = 0;
			break;
	}
}


